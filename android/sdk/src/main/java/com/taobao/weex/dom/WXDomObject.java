  /** package **/ void traverseTree(Consumer...consumers){
    if (consumers == null) {
      return;
    }

    for (Consumer consumer:consumers){
      consumer.accept(this);
    }

    int count = childCount();
    WXDomObject child;
    for (int i = 0; i < count; ++i) {
      child = getChild(i);
      child.traverseTree(consumers);
    }
  }


